const num3: number = 10;
const num4: number = 5;

const isEqual: boolean = num3 === num4;
const isNotEqual: boolean = num3 !== num4;
const isGreaterThan: boolean = num3 > num4;
const isLessThan: boolean = num3 < num4;

console.log(`Equality: ${num3} === ${num4} = ${isEqual}`);
console.log(`Inequality: ${num3} !== ${num4} = ${isNotEqual}`);
console.log(`Greater Than: ${num3} > ${num4} = ${isGreaterThan}`);
console.log(`Less Than: ${num3} < ${num4} = ${isLessThan}`);
