// for
console.log("For Loop:");
for (let i: number = 1; i <= 5; i++) {
  console.log(`Iteration ${i}`);
}

// while
console.log("While Loop:");
let j: number = 1;
while (j <= 5) {
  console.log(`Iteration ${j}`);
  j++;
}

// do-while
console.log("Do-While Loop:");
let k: number = 1;
do {
  console.log(`Iteration ${k}`);
  k++;
} while (k <= 5);